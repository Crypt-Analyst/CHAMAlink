def get_country_by_code(country_code):
    """Get country info by ISO code"""
    for country in ALL_COUNTRIES:
        if country['code'] == country_code:
            return country
    return None

def get_country_flag(country_code):
    """Get flag emoji for country code"""
    return COUNTRY_FLAGS.get(country_code, 'ğŸŒ')

def get_african_countries():
    """Get list of African countries"""
    return AFRICAN_COUNTRIES

def get_global_countries():
    """Get list of global countries"""
    return GLOBAL_COUNTRIES

def get_all_countries():
    """Get all countries sorted alphabetically"""
    return sorted(ALL_COUNTRIES, key=lambda x: x['name'])

def get_currency_for_country(country_code):
    """Get default currency for a country"""
    country = get_country_by_code(country_code)
    return country['currency'] if country else 'USD'
